    {
        cal.set(Calendar.MONTH, Calendar.JUNE);
    }
     if(smon==4)
    {
        cal.set(Calendar.MONTH, Calendar.MAY);
    }
      if(smon==3)
    {
        cal.set(Calendar.MONTH, Calendar.APRIL);
    }
       if(smon==2)
    {
        cal.set(Calendar.MONTH, Calendar.MARCH);
    }
           if(smon==1)
    {
        cal.set(Calendar.MONTH, Calendar.FEBRUARY);
    }
               if(smon==0)
    {
        cal.set(Calendar.MONTH, Calendar.JANUARY);
    }
   //  cal.set(Calendar.MONTH,smon);
     cal.set(Calendar.YEAR, yrr);

     cal.set(Calendar.DAY_OF_MONTH, 1);
     Date firstDayOfMonth = cal.getTime();  

     DateFormat sdf = new SimpleDateFormat("EEEEEEEE");   
     System.out.println("First Day of Month: " + mon.getSelectedItem().toString()+" "+ yr.getSelectedItem().toString()+" "+ mon.getSelectedItem().toString()+" "+ sdf.format(firstDayOfMonth)); 
     
     return sdf.format(firstDayOfMonth);
